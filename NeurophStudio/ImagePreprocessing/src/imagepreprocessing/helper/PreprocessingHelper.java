package imagepreprocessing.helper;

/**
 * Class that contains all filter independent methods
 * that are being used in image filters implementation
 * 
 * Note: utility class which contains only one method
 * 
 * @author Sanja
 */
public class PreprocessingHelper {

    public static int colorToRGB(int alpha, int red, int green, int blue) {

        int newPixel = 0;
        newPixel += alpha;
        newPixel = newPixel << 8;
        newPixel += red;
        newPixel = newPixel << 8;
        newPixel += green;
        newPixel = newPixel << 8;
        newPixel += blue;

        return newPixel;
    }
}
